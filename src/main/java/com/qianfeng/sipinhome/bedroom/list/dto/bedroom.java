package com.qianfeng.sipinhome.bedroom.list.dto;

public class bedroom {
    private int id;
    private String name;
    private String fie;

    public String getFie() {
        return fie;
    }

    public void setFie(String fie) {
        this.fie = fie;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
